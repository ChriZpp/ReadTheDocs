def addition(x, y):
    """ This is a simple addition function

    :param x: Numerical value
    :param y: Numerical value
    :return: Addition of the given parameters
    """
    return x + y


def subtraction(x, y):
    """ This is a simple subtraction function

    :param x: Numerical value
    :param y: Numerical value
    :return: Subtraction of the given parameters
    """
    return x - y


def multiplication(x, y):
    """ This is a simple multiplication function

    :param x: Numerical value
    :param y: Numerical value
    :return: Multiplication of the given parameters
    """
    return x * y


def division(x, y):
    """ This is a simple division function

    :param x: Numerical value
    :param y: Numerical value
    :return: Division of the given parameters
    """
    return x // y
